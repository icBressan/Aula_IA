numero1 = input("Informe um numero:")
numero2 = input("Informe outro numero:")

soma = float(numero1)+float(numero2)
sub = float(numero1)-float(numero2)
prod = float(numero1)*float(numero2)
div = float(numero1)/float(numero2)

print("A soma dos números é =",soma)
print("A subtração dos números é =",sub)
print("A multiplicação dos números é =",prod)
print("A divisão dos números é =",div)